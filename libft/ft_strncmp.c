#include "libft.h"

int	ft_strncmp(const char *str1, const char *str2, size_t size)
{
	unsigned int	i;

	if (size == 0)
		return (0);
	i = 0;
	while (str1[i] && str2[i] && i < (size - 1))
	{
		if ((unsigned char)str1[i] != (unsigned char)str2[i])
			return ((unsigned char)str1[i] - (unsigned char)str2[i]);
		i++;
	}
	return ((unsigned char)str1[i] - (unsigned char)str2[i]);
}
